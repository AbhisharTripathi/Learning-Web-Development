let h1 = document.querySelector("h1");
function changeColorUsingPromise(color, delay) {
    return new Promise((resolve, reject) => {
        setTimeout(() => {
            h1.style.color = color;
            if(color == "blue") reject("Human rejected.");//produce an error and will stop the execuion of any other statement after it if the function call for blue is await && (not inside try block || error is not generated by reject())
            resolve(`Color changed to ${color}`); //shown in PromiseResult and can be accessed using argument in then()
        }, delay);
    });
}

async function changeColorUsingPromiseAndAwait() {
    try {//error will not get caught if the function is not await or error is not generated by reject();
        await changeColorUsingPromise("red", 1000);
        await changeColorUsingPromise("green", 1000);
        await changeColorUsingPromise("blue", 1000);
        await changeColorUsingPromise("yellow", 1000);
        await changeColorUsingPromise("pink", 1000);
        await changeColorUsingPromise("grey", 1000);
    }
    catch(err) {
        console.log("Error is : ", err);
    }
    console.log("All await functions executed.");    
}

changeColorUsingPromiseAndAwait();




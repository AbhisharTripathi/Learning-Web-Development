//await : await keyword can only be used inside an async function and before a function that returns a promise object. await keyword will wait for the fulfilled status of the promise object returned by that function before the execution of the next statement. it returns the result of the promise.
function getNum() {
    return new Promise((resolve, reject) => {
        setTimeout(() =>{
        let num = Math.floor(Math.random() * 10 + 1);
        console.log(num);
        resolve(num);
    },1000);
    });
}

